int		ft_is_prime(int		nb)
{
	int		i;

	int		is_prime;

	i	=	2;
	is_prime	=	1;
	while(i	<=	nb/2)
		{
			if(nb % i ==	0)
			{
				is_prime	=	0;
			}
			i++;
		}
	if(is_prime	==	1)
		return(1);
	else
		return(0);
}
