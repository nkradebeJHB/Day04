int		ft_sqrt(int		nb)
{
	int		i;

	int		sqrt;

	sqrt	=	1;
	i		=	2;
	while(i	<=	nb/2)
	{
		if((i * i)	!=	nb)
			sqrt	=	0 ;
		i++;
	}
	if(sqrt	==	1)
		return(i);
	else
		return(0);
}
